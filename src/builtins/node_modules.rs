//! Node.js-style built-in modules: `fs`, `path`, `os`, `buffer`.
//! These are resolved when `require("fs")` etc. is called (bare specifiers).

use crate::error;
use crate::interp::{Interpreter, NativeFn};
use crate::value::*;
use crate::builtins::{def_method, def_const_value};
use std::rc::Rc;

/// Check if a specifier is a built-in module name, and return its namespace
/// object if so.
pub fn try_load_builtin(interp: &mut Interpreter, specifier: &str) -> Option<Value> {
    match specifier {
        "fs" => Some(make_fs_module(interp)),
        "path" => Some(make_path_module(interp)),
        "os" => Some(make_os_module(interp)),
        "buffer" | "Buffer" => Some(make_buffer_module(interp)),
        "util" => Some(make_util_module(interp)),
        "crypto" => Some(make_crypto_module(interp)),
        "events" => Some(make_events_module(interp)),
        "url" => Some(make_url_module(interp)),
        "querystring" => Some(make_querystring_module(interp)),
        _ => None,
    }
}

fn make_fs_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    // fs.readFileSync(path, encoding?) -> string | Uint8Array
    def_method(&realm, &m, "readFileSync", 2, Rc::new(|interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let encoding = args.get(1).cloned();
        let data = std::fs::read(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot read file '{}': {}", path, e))
        })?;
        // If encoding is provided, return a string; else return a Uint8Array.
        if let Some(enc) = encoding {
            if !enc.is_undefined() {
                let enc_str = crate::value::to_string(&enc);
                let s = if enc_str.eq_ignore_ascii_case("utf8") || enc_str.eq_ignore_ascii_case("utf-8") {
                    String::from_utf8_lossy(&data).into_owned()
                } else {
                    String::from_utf8_lossy(&data).into_owned()
                };
                return Ok(Value::from_string(s));
            }
        }
        // Return a Uint8Array
        let buf = Rc::new(std::cell::RefCell::new(data));
        let o = ObjectInner::new_object();
        o.borrow_mut().proto = Some(Value::Object(interp.realm().array_buffer_proto.clone()));
        o.borrow_mut().class = "ArrayBuffer";
        o.borrow_mut().kind = ObjectKind::ArrayBuffer(buf);
        Ok(Value::Object(o))
    }));
    // fs.writeFileSync(path, data) -> undefined
    def_method(&realm, &m, "writeFileSync", 2, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let data = args.get(1).cloned().unwrap_or(Value::Undefined);
        let bytes = match &data {
            Value::String(s) => s.as_bytes().to_vec(),
            Value::Object(o) => {
                let b = o.borrow();
                if let ObjectKind::ArrayBuffer(buf) = &b.kind {
                    buf.borrow().clone()
                } else if let ObjectKind::TypedArray { buffer, byte_offset, length, kind: TypedArrayKind::Uint8 } = &b.kind {
                    buffer.borrow()[*byte_offset..*byte_offset + *length].to_vec()
                } else if let ObjectKind::Array(items) = &b.kind {
                    items.iter().map(|v| to_int32(v) as u8).collect()
                } else {
                    crate::value::to_string(&data).into_bytes()
                }
            }
            _ => crate::value::to_string(&data).into_bytes(),
        };
        std::fs::write(&*path, bytes).map_err(|e| {
            error::throw_type(&format!("Cannot write file '{}': {}", path, e))
        })?;
        Ok(Value::Undefined)
    }));
    // fs.existsSync(path) -> boolean
    def_method(&realm, &m, "existsSync", 1, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        Ok(Value::Bool(std::path::Path::new(&*path).exists()))
    }));
    // fs.mkdirSync(path) -> undefined
    def_method(&realm, &m, "mkdirSync", 1, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        std::fs::create_dir_all(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot create directory '{}': {}", path, e))
        })?;
        Ok(Value::Undefined)
    }));
    // fs.readdirSync(path) -> string[]
    def_method(&realm, &m, "readdirSync", 1, Rc::new(|interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let entries = std::fs::read_dir(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot read directory '{}': {}", path, e))
        })?;
        let mut items = Vec::new();
        for entry in entries {
            if let Ok(e) = entry {
                let name = e.file_name().to_string_lossy().to_string();
                items.push(Value::from_string(name));
            }
        }
        Ok(interp.new_array(items))
    }));
    // fs.statSync(path) -> object with size, isFile, isDirectory
    def_method(&realm, &m, "statSync", 1, Rc::new(|interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let meta = std::fs::metadata(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot stat '{}': {}", path, e))
        })?;
        let size = meta.len() as i32;
        let is_file = meta.is_file();
        let is_dir = meta.is_dir();
        let mtime = meta.modified().ok().and_then(|t| t.duration_since(std::time::UNIX_EPOCH).ok()).map(|d| d.as_millis() as i32).unwrap_or(0);
        let o = ObjectInner::new_object();
        o.borrow_mut().proto = Some(Value::Object(interp.realm().object_proto.clone()));
        o.borrow_mut().props.insert(PropKey::from_str("size"), Property::data(Value::from_int(size)));
        o.borrow_mut().props.insert(PropKey::from_str("isFile"), Property::data(
            make_native_fn(&interp.realm().clone(), "isFile", 0, Rc::new(move |_, _, _| Ok(Value::Bool(is_file))))
        ));
        o.borrow_mut().props.insert(PropKey::from_str("isDirectory"), Property::data(
            make_native_fn(&interp.realm().clone(), "isDirectory", 0, Rc::new(move |_, _, _| Ok(Value::Bool(is_dir))))
        ));
        o.borrow_mut().props.insert(PropKey::from_str("mtime"), Property::data(Value::from_int(mtime)));
        Ok(Value::Object(o))
    }));
    // fs.unlinkSync(path)
    def_method(&realm, &m, "unlinkSync", 1, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        std::fs::remove_file(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot remove '{}': {}", path, e))
        })?;
        Ok(Value::Undefined)
    }));
    // fs.rmdirSync(path)
    def_method(&realm, &m, "rmdirSync", 1, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        std::fs::remove_dir_all(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot remove directory '{}': {}", path, e))
        })?;
        Ok(Value::Undefined)
    }));
    // fs.appendFileSync(path, data)
    def_method(&realm, &m, "appendFileSync", 2, Rc::new(|_interp, _this, args| {
        let path = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let data = crate::value::to_string(args.get(1).unwrap_or(&Value::Undefined));
        use std::io::Write;
        let mut f = std::fs::OpenOptions::new().append(true).create(true).open(&*path).map_err(|e| {
            error::throw_type(&format!("Cannot open '{}': {}", path, e))
        })?;
        f.write_all(data.as_bytes()).map_err(|e| {
            error::throw_type(&format!("Cannot write '{}': {}", path, e))
        })?;
        Ok(Value::Undefined)
    }));
    Value::Object(m)
}

fn make_path_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    def_method(&realm, &m, "join", 0, Rc::new(|_interp, _this, args| {
        let mut parts: Vec<String> = Vec::new();
        for a in args {
            let s = crate::value::to_string(a);
            if !s.is_empty() { parts.push(s); }
        }
        let joined = parts.join("/");
        let cleaned = joined.replace("//", "/");
        Ok(Value::from_string(cleaned))
    }));
    def_method(&realm, &m, "resolve", 0, Rc::new(|_interp, _this, args| {
        let mut parts: Vec<String> = Vec::new();
        for a in args {
            parts.push(crate::value::to_string(a));
        }
        Ok(Value::from_string(parts.join("/")))
    }));
    def_method(&realm, &m, "dirname", 1, Rc::new(|_interp, _this, args| {
        let p = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let dir = std::path::Path::new(&*p).parent().map(|x| x.to_string_lossy().to_string()).unwrap_or_else(|| ".".to_string());
        Ok(Value::from_string(dir))
    }));
    def_method(&realm, &m, "basename", 1, Rc::new(|_interp, _this, args| {
        let p = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let base = std::path::Path::new(&*p).file_name().map(|x| x.to_string_lossy().to_string()).unwrap_or_default();
        Ok(Value::from_string(base))
    }));
    def_method(&realm, &m, "extname", 1, Rc::new(|_interp, _this, args| {
        let p = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let ext = std::path::Path::new(&*p).extension().map(|x| format!(".{}", x.to_string_lossy())).unwrap_or_default();
        Ok(Value::from_string(ext))
    }));
    def_method(&realm, &m, "sep", 0, Rc::new(|_i, _t, _a| Ok(Value::from_str("/"))));
    def_const_value(&m, "delimiter", Value::from_str(":"));
    Value::Object(m)
}

fn make_os_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    def_method(&realm, &m, "platform", 0, Rc::new(|_i, _t, _a| Ok(Value::from_str("linux"))));
    def_method(&realm, &m, "arch", 0, Rc::new(|_i, _t, _a| Ok(Value::from_str("x64"))));
    def_method(&realm, &m, "homedir", 0, Rc::new(|_i, _t, _a| {
        Ok(Value::from_string(std::env::var("HOME").unwrap_or_else(|_| "/".to_string())))
    }));
    def_method(&realm, &m, "tmpdir", 0, Rc::new(|_i, _t, _a| {
        Ok(Value::from_string(std::env::var("TMPDIR").unwrap_or_else(|_| "/tmp".to_string())))
    }));
    def_method(&realm, &m, "hostname", 0, Rc::new(|_i, _t, _a| {
        Ok(Value::from_string(std::env::var("HOSTNAME").unwrap_or_else(|_| "localhost".to_string())))
    }));
    def_method(&realm, &m, "cpus", 0, Rc::new(|interp, _t, _a| {
        Ok(interp.new_array(vec![]))
    }));
    def_method(&realm, &m, "totalmem", 0, Rc::new(|_i, _t, _a| Ok(Value::from_int(0))));
    def_method(&realm, &m, "freemem", 0, Rc::new(|_i, _t, _a| Ok(Value::from_int(0))));
    def_method(&realm, &m, "type", 0, Rc::new(|_i, _t, _a| Ok(Value::from_str("Linux"))));
    def_method(&realm, &m, "release", 0, Rc::new(|_i, _t, _a| Ok(Value::from_str("1.0.0"))));
    def_method(&realm, &m, "uptime", 0, Rc::new(|_i, _t, _a| Ok(Value::from_int(0))));
    def_const_value(&m, "EOL", Value::from_str("\n"));
    Value::Object(m)
}

fn make_buffer_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    // Buffer constructor (simplified — returns a Uint8Array-like object)
    def_method(&realm, &m, "from", 1, Rc::new(|interp, _this, args| {
        let v = args.get(0).cloned().unwrap_or(Value::Undefined);
        match &v {
            Value::String(s) => {
                let bytes: Vec<u8> = s.as_bytes().to_vec();
                let items: Vec<Value> = bytes.iter().map(|b| Value::from_int(*b as i32)).collect();
                Ok(interp.new_array(items))
            }
            Value::Object(o) => {
                if let ObjectKind::Array(items) = &o.borrow().kind {
                    Ok(interp.new_array(items.clone()))
                } else {
                    Ok(interp.new_array(vec![]))
                }
            }
            _ => Ok(interp.new_array(vec![])),
        }
    }));
    def_method(&realm, &m, "alloc", 1, Rc::new(|interp, _this, args| {
        let n = to_int32(args.get(0).unwrap_or(&Value::from_int(0))) as usize;
        let items: Vec<Value> = vec![Value::from_int(0); n];
        Ok(interp.new_array(items))
    }));
    def_method(&realm, &m, "concat", 1, Rc::new(|interp, _this, args| {
        let arr = args.get(0).cloned().unwrap_or(Value::Undefined);
        let items = interp.iterable_to_vec(&arr)?;
        let mut out = Vec::new();
        for buf in items {
            if let Value::Object(o) = &buf {
                if let ObjectKind::Array(items) = &o.borrow().kind {
                    for it in items {
                        out.push(it.clone());
                    }
                }
            }
        }
        Ok(interp.new_array(out))
    }));
    def_method(&realm, &m, "byteLength", 1, Rc::new(|_i, _t, args| {
        let s = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        Ok(Value::from_int(s.as_bytes().len() as i32))
    }));
    // isBuffer
    def_method(&realm, &m, "isBuffer", 1, Rc::new(|_i, _t, args| {
        if let Some(Value::Object(o)) = args.get(0) {
            if let ObjectKind::Array(_) = &o.borrow().kind {
                return Ok(Value::Bool(true));
            }
        }
        Ok(Value::Bool(false))
    }));
    Value::Object(m)
}

fn make_native_fn(realm: &Rc<crate::realm::Realm>, name: &str, len: usize, f: NativeFn) -> Value {
    crate::interp::make_native_value(realm, name, len, f)
}

