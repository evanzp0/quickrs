// url module (simplified)
// ---------------------------------------------------------------------------

fn make_url_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    def_method(&realm, &m, "parse", 1, Rc::new(|interp, _this, args| {
        let url_str = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let o = ObjectInner::new_object();
        o.borrow_mut().proto = Some(Value::Object(interp.realm().object_proto.clone()));
        // Very simple URL parsing
        let url_str_owned = url_str.clone();
        let (protocol, rest) = if let Some(idx) = url_str.find("://") {
            (url_str[..idx].to_string(), &url_str_owned[idx+3..])
        } else {
            (String::new(), &url_str_owned[..])
        };
        let (host_port, path) = if let Some(idx) = rest.find('/') {
            (&rest[..idx], &rest[idx..])
        } else {
            (rest, "/")
        };
        let (host, port) = if let Some(idx) = host_port.find(':') {
            (&host_port[..idx], host_port[idx+1..].parse::<i32>().unwrap_or(0))
        } else {
            (host_port, 0)
        };
        let (pathname, search) = if let Some(idx) = path.find('?') {
            (&path[..idx], &path[idx..])
        } else {
            (path, "")
        };
        o.borrow_mut().props.insert(PropKey::from_str("href"), Property::data(Value::from_string(url_str)));
        o.borrow_mut().props.insert(PropKey::from_str("protocol"), Property::data(Value::from_string(protocol)));
        o.borrow_mut().props.insert(PropKey::from_str("host"), Property::data(Value::from_string(host.to_string())));
        o.borrow_mut().props.insert(PropKey::from_str("port"), Property::data(Value::from_int(port)));
        o.borrow_mut().props.insert(PropKey::from_str("pathname"), Property::data(Value::from_string(pathname.to_string())));
        o.borrow_mut().props.insert(PropKey::from_str("search"), Property::data(Value::from_string(search.to_string())));
        Ok(Value::Object(o))
    }));
    def_method(&realm, &m, "resolve", 2, Rc::new(|_i, _t, args| {
        let from = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let to = crate::value::to_string(args.get(1).unwrap_or(&Value::Undefined));
        // Simplified: if `to` is absolute, return it; else join
        if to.starts_with("/") || to.contains("://") {
            Ok(Value::from_string(to))
        } else {
            let base = from.rfind('/').map(|i| &from[..i+1]).unwrap_or("/");
            Ok(Value::from_string(format!("{}{}", base, to)))
        }
    }));
    Value::Object(m)
}

