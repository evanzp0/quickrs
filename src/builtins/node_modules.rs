fn make_crypto_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    // crypto.createHash(algorithm) -> Hash object
    def_method(&realm, &m, "createHash", 1, Rc::new(|interp, _this, args| {
        let algo = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let algo_lower = algo.to_lowercase();
        // Collect data via update() then digest()
        let data = Rc::new(std::cell::RefCell::new(Vec::<u8>::new()));
        let data_clone = data.clone();
        let algo_for_update = algo_lower.clone();
        let update_fn = crate::interp::make_native_value(interp.realm(), "update", 1, Rc::new(move |_interp, _this, args| {
            let input = args.get(0).cloned().unwrap_or(Value::Undefined);
            let bytes = match &input {
                Value::String(s) => s.as_bytes().to_vec(),
                Value::Object(o) => {
                    if let ObjectKind::Array(items) = &o.borrow().kind {
                        items.iter().map(|v| to_int32(v) as u8).collect()
                    } else {
                        crate::value::to_string(&input).into_bytes()
                    }
                }
                _ => crate::value::to_string(&input).into_bytes(),
            };
            data_clone.borrow_mut().extend(bytes);
            // Return this (the hash object) for chaining — but we don't have a ref.
            // Return undefined instead (simplified).
            let _ = algo_for_update;
            Ok(Value::Undefined)
        }));
        let data_for_digest = data.clone();
        let algo_for_digest = algo_lower.clone();
        let digest_fn = crate::interp::make_native_value(interp.realm(), "digest", 1, Rc::new(move |_interp, _this, args| {
            let encoding = args.get(0).map(|v| crate::value::to_string(v)).unwrap_or_else(|| "hex".to_string());
            let bytes = data_for_digest.borrow().clone();
            let hash_bytes = match algo_for_digest.as_str() {
                "md5" => {
                    use md5::{Md5, Digest};
                    let mut h = Md5::new();
                    h.update(&bytes);
                    h.finalize().to_vec()
                }
                "sha1" => {
                    use sha1::{Sha1, Digest};
                    let mut h = Sha1::new();
                    h.update(&bytes);
                    h.finalize().to_vec()
                }
                "sha256" => {
                    use sha2::{Sha256, Digest};
                    let mut h = Sha256::new();
                    h.update(&bytes);
                    h.finalize().to_vec()
                }
                "sha512" => {
                    use sha2::{Sha512, Digest};
                    let mut h = Sha512::new();
                    h.update(&bytes);
                    h.finalize().to_vec()
                }
                _ => return Ok(Value::Undefined),
            };
            let result = if encoding == "hex" {
                hex::encode(&hash_bytes)
            } else if encoding == "base64" {
                // simple base64
                let alpha = b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
                let mut out = String::new();
                let mut i = 0;
                while i + 3 <= hash_bytes.len() {
                    let n = ((hash_bytes[i] as u32) << 16) | ((hash_bytes[i+1] as u32) << 8) | hash_bytes[i+2] as u32;
                    out.push(alpha[((n >> 18) & 63) as usize] as char);
                    out.push(alpha[((n >> 12) & 63) as usize] as char);
                    out.push(alpha[((n >> 6) & 63) as usize] as char);
                    out.push(alpha[(n & 63) as usize] as char);
                    i += 3;
                }
                let rem = hash_bytes.len() - i;
                if rem == 1 {
                    let n = (hash_bytes[i] as u32) << 16;
                    out.push(alpha[((n >> 18) & 63) as usize] as char);
                    out.push(alpha[((n >> 12) & 63) as usize] as char);
                    out.push_str("==");
                } else if rem == 2 {
                    let n = ((hash_bytes[i] as u32) << 16) | ((hash_bytes[i+1] as u32) << 8);
                    out.push(alpha[((n >> 18) & 63) as usize] as char);
                    out.push(alpha[((n >> 12) & 63) as usize] as char);
                    out.push(alpha[((n >> 6) & 63) as usize] as char);
                    out.push('=');
                }
                out
            } else {
                hex::encode(&hash_bytes)
            };
            Ok(Value::from_string(result))
        }));
        // Create the hash object
        let hash_obj = ObjectInner::new_object();
        hash_obj.borrow_mut().proto = Some(Value::Object(interp.realm().object_proto.clone()));
        hash_obj.borrow_mut().class = "Hash";
        hash_obj.borrow_mut().props.insert(PropKey::from_str("update"), Property::data(update_fn));
        hash_obj.borrow_mut().props.insert(PropKey::from_str("digest"), Property::data(digest_fn));
        Ok(Value::Object(hash_obj))
    }));
    // crypto.randomBytes(size) -> number[]
    def_method(&realm, &m, "randomBytes", 1, Rc::new(|interp, _this, args| {
        let n = to_int32(args.get(0).unwrap_or(&Value::from_int(0))) as usize;
        let mut bytes = Vec::with_capacity(n);
        for _ in 0..n {
            // Use a simple PRNG for portability
            use std::cell::Cell;
            thread_local! { static STATE: Cell<u64> = Cell::new(0x9e3779b97f4a7c15); }
            let v = STATE.with(|s| {
                let mut x = s.get();
                if x == 0 { x = std::time::SystemTime::now().duration_since(std::time::UNIX_EPOCH).unwrap().as_nanos() as u64 | 1; }
                x ^= x << 13; x ^= x >> 7; x ^= x << 17;
                s.set(x);
                x
            });
            bytes.push((v & 0xff) as u8);
        }
        let items: Vec<Value> = bytes.iter().map(|b| Value::from_int(*b as i32)).collect();
        Ok(interp.new_array(items))
    }));
    // crypto.timingSafeEqual (simplified)
    def_method(&realm, &m, "timingSafeEqual", 2, Rc::new(|_i, _t, args| {
        let a = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let b = crate::value::to_string(args.get(1).unwrap_or(&Value::Undefined));
        Ok(Value::Bool(a == b))
    }));
    Value::Object(m)
}

