fn make_util_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    // util.format(format, ...args) — printf-like formatting
    def_method(&realm, &m, "format", 1, Rc::new(|_interp, _this, args| {
        if args.is_empty() { return Ok(Value::from_str("")); }
        let fmt = crate::value::to_string(&args[0]);
        let mut out = String::new();
        let mut arg_idx = 1;
        let mut chars = fmt.chars().peekable();
        while let Some(c) = chars.next() {
            if c == '%' {
                match chars.next() {
                    Some('s') => {
                        if arg_idx < args.len() { out.push_str(&crate::value::to_string(&args[arg_idx])); arg_idx += 1; }
                    }
                    Some('d') => {
                        if arg_idx < args.len() { out.push_str(&crate::value::format_number(crate::value::to_number(&args[arg_idx]))); arg_idx += 1; }
                    }
                    Some('j') => {
                        if arg_idx < args.len() { out.push_str(&crate::value::to_string(&args[arg_idx])); arg_idx += 1; }
                    }
                    Some('%') => out.push('%'),
                    Some(c) => { out.push('%'); out.push(c); }
                    None => out.push('%'),
                }
            } else {
                out.push(c);
            }
        }
        // Append remaining args
        while arg_idx < args.len() {
            out.push(' ');
            out.push_str(&crate::value::to_string(&args[arg_idx]));
            arg_idx += 1;
        }
        Ok(Value::from_string(out))
    }));
    // util.inspect(value) — pretty-print (uses console.log formatting)
    def_method(&realm, &m, "inspect", 1, Rc::new(|interp, _this, args| {
        let v = args.get(0).cloned().unwrap_or(Value::Undefined);
        Ok(Value::from_string(crate::builtins::pretty_print(&v, interp, 0)))
    }));
    // util.types
    let types = ObjectInner::new_object();
    types.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    def_method(&realm, &types, "isString", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::String(_)))))));
    def_method(&realm, &types, "isNumber", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::Number(_)))))));
    def_method(&realm, &types, "isBoolean", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::Bool(_)))))));
    def_method(&realm, &types, "isUndefined", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::Undefined) | None)))));
    def_method(&realm, &types, "isNull", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::Null))))));
    def_method(&realm, &types, "isObject", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(matches!(a.get(0), Some(Value::Object(_)))))));
    def_method(&realm, &types, "isFunction", 1, Rc::new(|_i, _t, a| Ok(Value::Bool(a.get(0).map(|v| v.is_callable()).unwrap_or(false)))));
    def_method(&realm, &types, "isArray", 1, Rc::new(|_i, _t, a| {
        if let Some(Value::Object(o)) = a.get(0) {
            return Ok(Value::Bool(matches!(o.borrow().kind, ObjectKind::Array(_))));
        }
        Ok(Value::Bool(false))
    }));
    def_method(&realm, &types, "isPromise", 1, Rc::new(|_i, _t, a| {
        if let Some(Value::Object(o)) = a.get(0) {
            return Ok(Value::Bool(matches!(o.borrow().kind, ObjectKind::Promise(_))));
        }
        Ok(Value::Bool(false))
    }));
    m.borrow_mut().props.insert(PropKey::from_str("types"), Property::data(Value::Object(types)));
    // util.promisify(fn) — convert callback-style to Promise
    def_method(&realm, &m, "promisify", 1, Rc::new(|interp, _this, args| {
        let orig = args.get(0).cloned().unwrap_or(Value::Undefined);
        let realm = interp.realm().clone();
        let f = crate::interp::make_native_value(&realm, "promisified", 0, Rc::new(move |interp, _this, pargs| {
            let p = interp.new_promise();
            let p_clone = p.clone();
            let callback = crate::interp::make_native_value(interp.realm(), "callback", 0, Rc::new(move |interp, _cbthis, cbargs| {
                let err = cbargs.get(0).cloned().unwrap_or(Value::Undefined);
                if !err.is_nullish() {
                    interp.reject_promise(p_clone.clone(), err);
                } else {
                    let results: Vec<Value> = cbargs.iter().skip(1).cloned().collect();
                    let val = if results.len() == 1 { results[0].clone() } else { interp.new_array(results) };
                    interp.resolve_promise(p_clone.clone(), val);
                }
                Ok(Value::Undefined)
            }));
            let mut all_args = pargs.to_vec();
            all_args.push(callback);
            let _ = interp.call_value(orig.clone(), Value::Undefined, &all_args);
            Ok(p)
        }));
        Ok(f)
    }));
    def_method(&realm, &m, "isDeepStrictEqual", 2, Rc::new(|_i, _t, args| {
        let a = args.get(0).cloned().unwrap_or(Value::Undefined);
        let b = args.get(1).cloned().unwrap_or(Value::Undefined);
        Ok(Value::Bool(crate::value::to_string(&a) == crate::value::to_string(&b)))
    }));
    Value::Object(m)
}

