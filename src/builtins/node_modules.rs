fn make_events_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    // Create prototype first
    let proto = ObjectInner::new_object();
    proto.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    proto.borrow_mut().class = "EventEmitter";
    // EventEmitter constructor
    let proto_for_ctor = proto.clone();
    let call_fn: NativeFn = Rc::new(|_i, _t, _a| {
        Err(error::throw_type("Constructor EventEmitter requires 'new'"))
    });
    let proto_for_ctor2 = proto.clone();
    let ctor_fn: crate::builtins::CtorFn = Rc::new(move |interp, _this, _args, _nt| {
        let o = ObjectInner::new_object();
        o.borrow_mut().proto = Some(Value::Object(proto_for_ctor2.clone()));
        o.borrow_mut().class = "EventEmitter";
        let listeners = ObjectInner::new_object();
        listeners.borrow_mut().proto = Some(Value::Object(interp.realm().object_proto.clone()));
        o.borrow_mut().props.insert(PropKey::from_str("_listeners"), Property::data(Value::Object(listeners)));
        Ok(Value::Object(o))
    });
    let ctor = crate::builtins::make_ctor(&realm, "EventEmitter", 0, call_fn, ctor_fn);
    // Set ctor.prototype = proto
    if let Value::Object(co) = &ctor {
        co.borrow_mut().props.insert(PropKey::from_str("prototype"), Property {
            kind: PropKind::Data(Value::Object(proto_for_ctor.clone())),
            writable: false, enumerable: false, configurable: false,
        });
    }
    // EventEmitter.prototype methods
    def_method(&realm, &proto_for_ctor, "on", 2, Rc::new(|interp, this, args| {
        let event = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let listener = args.get(1).cloned().unwrap_or(Value::Undefined);
        let listeners_obj = interp.get_property(&this, &PropKey::from_str("_listeners"))?;
        let arr = interp.get_property(&listeners_obj, &PropKey::from_str(&event)).unwrap_or(Value::Undefined);
        if arr.is_undefined() {
            let new_arr = interp.new_array(vec![listener]);
            if let Value::Object(o) = &listeners_obj {
                o.borrow_mut().props.insert(PropKey::from_str(&event), Property::data(new_arr));
            }
        } else {
            let push_fn = interp.get_property(&arr, &PropKey::from_str("push"))?;
            interp.call_value(push_fn, arr, &[listener])?;
        }
        Ok(this)
    }));
    def_method(&realm, &proto_for_ctor, "emit", 1, Rc::new(|interp, this, args| {
        let event = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let emit_args: Vec<Value> = args.iter().skip(1).cloned().collect();
        let listeners_obj = interp.get_property(&this, &PropKey::from_str("_listeners"))?;
        let arr = interp.get_property(&listeners_obj, &PropKey::from_str(&event)).unwrap_or(Value::Undefined);
        if arr.is_undefined() { return Ok(Value::Bool(false)); }
        let items = interp.iterable_to_vec(&arr)?;
        for listener in items {
            interp.call_value(listener, Value::Undefined, &emit_args)?;
        }
        Ok(Value::Bool(true))
    }));
    def_method(&realm, &proto_for_ctor, "once", 2, Rc::new(|interp, this, args| {
        let event = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let listener = args.get(1).cloned().unwrap_or(Value::Undefined);
        let listener_clone = listener.clone();
        let wrapper = crate::interp::make_native_value(interp.realm(), "once_wrapper", 0, Rc::new(move |interp, _t, wargs| {
            interp.call_value(listener_clone.clone(), Value::Undefined, wargs)?;
            Ok(Value::Undefined)
        }));
        let on_fn = interp.get_property(&this, &PropKey::from_str("on"))?;
        interp.call_value(on_fn, this, &[Value::from_string(event), wrapper])
    }));
    def_method(&realm, &proto_for_ctor, "off", 2, Rc::new(|_interp, this, _args| {
        // Simplified: can't compare function identity
        Ok(this)
    }));
    def_method(&realm, &proto_for_ctor, "removeAllListeners", 1, Rc::new(|interp, this, args| {
        let event = args.get(0).map(|v| crate::value::to_string(v));
        let listeners_obj = interp.get_property(&this, &PropKey::from_str("_listeners"))?;
        if let Some(ev) = event {
            if let Value::Object(o) = &listeners_obj {
                o.borrow_mut().props.remove(&PropKey::from_str(&ev));
            }
        } else {
            if let Value::Object(o) = &listeners_obj {
                o.borrow_mut().props.clear();
            }
        }
        Ok(this)
    }));
    def_method(&realm, &proto_for_ctor, "listenerCount", 1, Rc::new(|interp, this, args| {
        let event = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let listeners_obj = interp.get_property(&this, &PropKey::from_str("_listeners"))?;
        let arr = interp.get_property(&listeners_obj, &PropKey::from_str(&event)).unwrap_or(Value::Undefined);
        if arr.is_undefined() { return Ok(Value::from_int(0)); }
        let items = interp.iterable_to_vec(&arr)?;
        Ok(Value::from_int(items.len() as i32))
    }));
    let _ = proto;
    m.borrow_mut().props.insert(PropKey::from_str("EventEmitter"), Property::data(ctor));
    Value::Object(m)
}

