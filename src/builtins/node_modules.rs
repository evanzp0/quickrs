// querystring module
// ---------------------------------------------------------------------------

fn make_querystring_module(interp: &mut Interpreter) -> Value {
    let realm = interp.realm().clone();
    let m = ObjectInner::new_object();
    m.borrow_mut().proto = Some(Value::Object(realm.object_proto.clone()));
    m.borrow_mut().class = "Module";
    def_method(&realm, &m, "parse", 1, Rc::new(|interp, _this, args| {
        let qs = crate::value::to_string(args.get(0).unwrap_or(&Value::Undefined));
        let o = ObjectInner::new_object();
        o.borrow_mut().proto = Some(Value::Object(interp.realm().object_proto.clone()));
        for pair in qs.split('&') {
            if let Some(idx) = pair.find('=') {
                let key = pair[..idx].to_string();
                let val = pair[idx+1..].to_string();
                o.borrow_mut().props.insert(PropKey::from_str(&key), Property::data(Value::from_string(val)));
            } else if !pair.is_empty() {
                o.borrow_mut().props.insert(PropKey::from_str(pair), Property::data(Value::from_str("")));
            }
        }
        Ok(Value::Object(o))
    }));
    def_method(&realm, &m, "stringify", 1, Rc::new(|interp, _this, args| {
        let obj = args.get(0).cloned().unwrap_or(Value::Undefined);
        let keys = interp.own_property_keys(&obj)?;
        let mut parts = Vec::new();
        for k in keys {
            if let PropKey::Str(s) = &k {
                let v = interp.get_property(&obj, &k)?;
                parts.push(format!("{}={}", s, crate::value::to_string(&v)));
            }
        }
        Ok(Value::from_string(parts.join("&")))
    }));
    Value::Object(m)
}
